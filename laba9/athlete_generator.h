static void generate_random_athletes(Athlete* athletes, int count) {
    int surnames_count = sizeof(randomSurnames) / sizeof(randomSurnames[0]);
    int firstnames_count = sizeof(randomFirstNames) / sizeof(randomFirstNames[0]);
    int patronymics_count = sizeof(randomPatronymics) / sizeof(randomPatronymics[0]);
    int sports_count = sizeof(randomSports) / sizeof(randomSports[0]);
    int ranks_count = sizeof(randomRanks) / sizeof(randomRanks[0]);

    for (int i = 0; i < count; i++) {
        
        snprintf(athletes[i].name, sizeof athletes[i].name,
            "%s %s %s",
            randomSurnames[rand() % surnames_count],
            randomFirstNames[rand() % firstnames_count],
            randomPatronymics[rand() % patronymics_count]
        );

        
        athletes[i].age = 16 + rand() % 20;

        
        athletes[i].height = 150.0f + (rand() % 50);

       
        athletes[i].weight = 50.0f + (rand() % 50);

        
        strncpy(athletes[i].sport,
            randomSports[rand() % sports_count],
            sizeof athletes[i].sport - 1);
        athletes[i].sport[sizeof athletes[i].sport - 1] = '\0';

        
        strncpy(athletes[i].rank,
            randomRanks[rand() % ranks_count],
            sizeof athletes[i].rank - 1);
        athletes[i].rank[sizeof athletes[i].rank - 1] = '\0';
    }
}

